//Написати функцію pow(x,y) яка буде приймати 2 числа, перше - це число яке ми будемо зводити у ступінь, друге - у яку ступінь.
//Наприклад pow(2,3) функція поверне значення 8, тобто два в кубі.


function pow(x, y) {
    for (var i = 1, j = 0; j < y; j++ ) {
        i = i * x;
        
    }
    let result = i; // зберігаємо значення i у змінну result
    return result;  // повертаємо result
}

let result = pow(-2, 4);
